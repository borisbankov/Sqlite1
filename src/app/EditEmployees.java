    private void jList1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseReleased
        int arrayIndex = jList1.getSelectedIndex();
        String[] row = employees.get(arrayIndex).split("---");
        jTextField1.setText(row[1]);
        jTextField2.setText(row[2]);
        jTextField3.setText(row[3]);
        jTextField4.setText(row[4]);
        jTextField5.setText(row[5]);
        id = Integer.parseInt(row[0]);
        if(!row[6].equals("null")){
            for (int i = 0; i < jComboBox1.getItemCount(); i++) {
                String reportsTo = jComboBox1.getItemAt(i).split(" ")[0];
                if(row[6].equals(reportsTo)){
                    jComboBox1.setSelectedIndex(i);
                }
            }
        } else {
            jComboBox1.setSelectedIndex(0);
        }
        
    }//GEN-LAST:event_jList1MouseReleased

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String firstName    = jTextField1.getText();
        String lastName     = jTextField2.getText();
        String title        = jTextField3.getText();
        String city         = jTextField4.getText();
        String country      = jTextField5.getText();
        
        String reportsTo = "";
        String selected = String.valueOf(jComboBox1.getSelectedItem());
        String checkSelected = selected.split(" ")[0];
        if(!checkSelected.equals("")){
            reportsTo = checkSelected;
        }
        query.update(new String[]{ "FirstName", "LastName", "Title", "City", "Country", "ReportsTo"},
                     new String[]{  firstName,   lastName,   title,  city,    country,  reportsTo},
                     "EmployeeId", id, "Employees");
        refreshList();
        comboRefresh();
    }//GEN-LAST:event_jButton1ActionPerformed

    void refreshList(){
        list.clear();
        jList1.setModel(list);
        employees = query.select(new String[]{
                                            "EmployeeId",
                                            "FirstName", 
                                            "LastName",
                                            "Title",
                                            "City",
                                            "Country",
                                            "ReportsTo"}, 
                              "Employees");
        for (int i = 0; i < employees.size(); i++) {
            String[] row = employees.get(i).split("---");
            list.addElement(row[0] + " " + row[1] + " " + row[2]);
        }
        
    }
    
     void comboRefresh(){
        jComboBox1.removeAllItems();
        jComboBox1.addItem("");
        for (int i = 0; i < employees.size(); i++) {
            String[] row = employees.get(i).split("---");
            String result = row[0] + " " + row[1];
            jComboBox1.addItem(result);
            
        }
        
    }
    
