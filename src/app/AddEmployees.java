    void tableRefresh(){
        t = (DefaultTableModel) jTable1.getModel();
        t.setRowCount(0);
        ArrayList<String> employees = new ArrayList<String>();
        employees = db.select(new String[]{ 
                                            "FirstName", 
                                            "LastName",
                                            "Title",
                                            "City",
                                            "Country",
                                            "ReportsTo"}, 
                              "Employees");
        for (int i = 0; i < employees.size(); i++) {
            String[] row = employees.get(i).split("---");
            t.addRow(new Object[]{
                                  row[0],                                  
                                  row[1],
                                  row[2],
                                  row[3],
                                  row[4],
                                  row[5].equals("null") ? "няма началник" : row[5] });
        }
    }
    
    void comboRefresh(){
        jComboBox1.removeAllItems();
        jComboBox1.addItem("");
        ArrayList<String> employees = new ArrayList<String>();
        employees = db.select(new String[]{"EmployeeId", "FirstName"}, "Employees");
        for (int i = 0; i < employees.size(); i++) {
            String[] row = employees.get(i).split("---");
            String result = String.join(" ", row);
            jComboBox1.addItem(result);
        }
        
    }
    
    
